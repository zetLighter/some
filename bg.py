days = {
    1: "Понедельник",
    2: "Вторник",
    3: "Среда",
    4: "Четверг",
    5: "Пятница",
    6: "Суббота",
    7: "Воскресенье"
}

while True:
    day = input("Введите число от 1 до 7: ")
    try:
        day = int(day)
        print(days[day])
    except:
        print("Неправильный ввод")
